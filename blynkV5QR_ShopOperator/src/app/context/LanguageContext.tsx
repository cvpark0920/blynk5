const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    // ì´ˆê¸°ê°’: localStorage ë˜ëŠ” ë¸Œë¼ìš°ì € ì–¸ì–´
    const savedLang = localStorage.getItem('blynk_shop_lang') as Language;
    if (savedLang && ['ko', 'vn', 'en', 'zh', 'ru'].includes(savedLang)) {
      return savedLang;
    }
    
    // ë¸Œë¼ìš°ì € ì–¸ì–´ ê°ì§€
    const browserLang = navigator.language.toLowerCase();
    if (browserLang.includes('ko')) return 'ko';
    if (browserLang.includes('vi')) return 'vn';
    if (browserLang.includes('zh')) return 'zh';
    if (browserLang.includes('ru')) return 'ru';
    return 'ko'; // ìƒì ì•± ê¸°ë³¸ê°’ì€ í•œêµ­ì–´
  });

  const setLanguage = (newLang: Language) => {
    setLanguageState(newLang);
    localStorage.setItem('blynk_shop_lang', newLang);
  };

  // localStorage ë³€ê²½ ê°ì§€ (ë‹¤ë¥¸ íƒ­ì—ì„œ ë³€ê²½ ì‹œ)
  useEffect(() => {
    const handleStorageChange = (e: StorageEvent) => {
      if (e.key === 'blynk_shop_lang' && e.newValue) {
        const newLang = e.newValue as Language;
        if (['ko', 'vn', 'en', 'zh', 'ru'].includes(newLang)) {
          setLanguageState(newLang);
        }
      }
    };

    window.addEventListener('storage', handleStorageChange);
    return () => window.removeEventListener('storage', handleStorageChange);
  }, []);

  const t = (key: string) => {
    // @ts-ignore
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}
